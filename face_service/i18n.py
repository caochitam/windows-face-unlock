_current_lang = DEFAULT_LANG
_lock = threading.Lock()


def detect_system_language() -> str:
    """Best-effort pick from locale, restricted to supported LANG_CODES."""
    try:
        code, _enc = locale.getlocale()
        if not code:
            code = locale.getdefaultlocale()[0] or ""
    except Exception:
        code = ""
    short = (code or "").split("_")[0].split("-")[0].lower()
    if short in TRANSLATIONS:
        return short
    return DEFAULT_LANG


def set_language(lang: str) -> None:
    """Set active language. Unknown codes fall back to English."""
    global _current_lang
    with _lock:
        _current_lang = lang if lang in TRANSLATIONS else DEFAULT_LANG


def get_language() -> str:
    return _current_lang


def t(key: str, **kwargs) -> str:
    """Translate ``key`` using the active language, with ``str.format`` kwargs.

    Fallback order: current language → English → raw key (so missing keys
    are at least visible instead of KeyError).
    """
    with _lock:
        lang = _current_lang
    translations = TRANSLATIONS.get(lang) or TRANSLATIONS[DEFAULT_LANG]
    s = translations.get(key) or TRANSLATIONS[DEFAULT_LANG].get(key, key)
    if kwargs:
        try:
            return s.format(**kwargs)
        except Exception:
            return s
    return s


def tr_languages() -> Iterable[tuple[str, str, str]]:
    """Yield (code, native_name, emoji) for the picker."""
    return LANGUAGES
